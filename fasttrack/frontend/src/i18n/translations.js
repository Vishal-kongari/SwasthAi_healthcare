// Get nested value: getTranslation(translations.en, 'nav.home') -> translations.en.navHome
export function getTranslation(langObj, key) {
  if (!langObj || !key) return key;
  const k = key.replace(/\./g, ''); // allow "nav.home" -> navHome
  const direct = langObj[key] ?? langObj[k];
  if (direct !== undefined) return direct;
  return key;
}
